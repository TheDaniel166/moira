"""
moira/comets.py — Comet Oracle

Archetype: Oracle
Purpose: Provides geocentric tropical ecliptic positions for astrologically
         curated periodic comets from a locally generated JPL SPK kernel.

Boundary declaration
--------------------
Owns:
    - COMET_NAIF         — name → NAIF ID mapping for supported comets.
    - CometData          — geocentric ecliptic position result dataclass.
    - load_comet_kernel() — explicit kernel load / reload.
    - comet_at()         — geocentric ecliptic position of one comet at a JD.
    - all_comets_at()    — positions of all (or a subset of) comets at a JD.
    - list_comets()      — all known comet names.
Delegates:
    - SPK file I/O and type 13 segment reading to moira._spk_body_kernel.
    - Earth/Sun barycentric positions to moira.planets / moira.spk_reader.
    - Light-time, aberration, deflection, frame-bias corrections to
      moira.corrections.
    - Precession and nutation matrices to moira.coordinates.
    - Obliquity to moira.obliquity.
    - Precession in longitude to moira.precession.
    - JD conversion to moira.julian.

Import-time side effects:
    - Importing moira._spk_body_kernel registers _Type13Segment in jplephem's
      _segment_classes dict.  This is idempotent.

External dependency assumptions:
    - jplephem must be installed (ImportError raised otherwise).
    - comets.bsp must be present (generated by scripts/build_comet_kernel.py)
      before any position query; FileNotFoundError is raised otherwise.

Public surface / exports:
    CometData             — position result dataclass
    COMET_NAIF            — name → NAIF ID dict
    comet_at()            — single-comet position
    all_comets_at()       — multi-comet positions
    list_comets()         — all known names
    load_comet_kernel()   — explicit kernel load / reload

NAIF ID convention for comets
------------------------------
JPL Horizons assigns comets NAIF IDs in the 1000xxx range (not 2000xxx).
Periodic comet P/N uses NAIF ID 1_000_000 + N, e.g.:
    1P/Halley           → 1000001
    2P/Encke            → 1000002
    9P/Tempel 1         → 1000009
    67P/Churyumov-Gerasimenko → 1000067
    109P/Swift-Tuttle   → 1000109

The kernel file (comets.bsp) must be generated locally via
scripts/build_comet_kernel.py, which fetches Horizons VECTORS for each
comet and writes a type 13 SPK with S/C center = 10 (Sun).

Usage
-----
    from moira.comets import comet_at, all_comets_at, list_comets

    pos = comet_at("Halley", jd_ut)
    print(pos.longitude, pos.sign, pos.retrograde)
"""

